            async function triggerShare() {
                const shareUrl = `${trendEntryUrl}?start=${encodeURIComponent(String(userId))}`;
                const text = `Мне выпал стратегический тренд 2026: ${currentCardData.title}\n\nЗагляни в будущее и собери свою коллекцию инсайтов 👇`;
                const fullText = text + '\n' + shareUrl;

                if (trendPlatform === 'telegram' && window.parent !== window) {
                    window.parent.postMessage({
                        type: 'mirofaktura:share',
                        title: 'Тренды 2026',
                        text,
                        url: shareUrl
                    }, '*');
                    return;
                }

                const fallbackCopy = () => {
                    const t = document.createElement('textarea'); 
                    t.value = fullText; 
                    t.setAttribute('readonly', ''); 
                    t.style.cssText = 'position:fixed; left:-9999px; opacity:0;'; 
                    document.body.appendChild(t); 
                    t.select(); 
                    t.setSelectionRange(0, 99999); 
                    
                    try {
                        const success = document.execCommand('copy'); 
                        if (!success) throw new Error('execCommand failed');
                        showCustomAlert('<div style="font-weight:800; color:#1F2E2E;">Ссылка скопирована! Отправьте её коллегам.</div>');
                    } catch(e) {
                        showCustomAlert('<div style="font-weight:800; color:#1F2E2E;">Не удалось скопировать. Пожалуйста, поделитесь ссылкой вручную.</div>');
                    } finally {
                        document.body.removeChild(t);
                    }
                };

                // Пробуем нативный шеринг (сработает на смартфонах и планшетах)
                if (navigator.share) {
                    try {
                        await navigator.share({
                            title: 'Тренды 2026',
                            text: text,
                            url: shareUrl
                        });
                        return; // Успешно поделились нативно
                    } catch (err) {
                        // Если пользователь просто закрыл шторку "Поделиться", не показываем ошибку
                        if (err.name !== 'AbortError') fallbackCopy();
                        return;
                    }
                }

                // Если нет поддержки share api (например, старый браузер или ПК)
                fallbackCopy();
            }

